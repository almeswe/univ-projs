static uint32_t last_click_tm = 0;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	// if switch mode button pressed
	if (HAL_GetTick() - last_click_tm >= 150) {
		last_click_tm = HAL_GetTick();
	}
	else {
		return;
	}
	if (GPIO_Pin == GPIO_PIN_0) {
		mode = !mode;
	}
	if (mode == LED_MODE_BTN) {
		int8_t value = 0;
		switch (GPIO_Pin) {
			case GPIO_PIN_1: value = 10;  break;
			case GPIO_PIN_4: value = -10; break;
			default: break;
		}
		int8_t edge = ((int8_t)led_btn_percent) + value;
		if (edge >= 0 && edge <= 100) {
			led_btn_percent = (uint8_t)edge;
		}
	}
}
